*******************************************************************************
* MACRO		68K_DisableINT	
* DESCRIPTION:	Disable INT on the 68000
* PARAMETERS:	
*******************************************************************************
68K_DisableINT	macro	
	move.w	sr,-(sp)		; disable interrupt
	or.w	#$700,sr
	endm

*******************************************************************************
* MACRO		68K_EnableINT	
* DESCRIPTION:	Disable INT on the 68000
* PARAMETERS:	
*******************************************************************************
68K_EnableINT	macro
	move.w	(sp)+,sr 
	endm
